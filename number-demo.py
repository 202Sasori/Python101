# Float và Int
h1_int = 1
h1_float = 2.0

print(h1_int)
print(h1_float)

print(type(h1_int))
print(type(h1_float))
# Complex
h1_complex = 3.14j

print(h1_complex)
print(type(h1_complex))
# Hex
h1_hex = 0xa
print(h1_hex)
print(type(h1_hex))
# Octal
h1_octal = 0o10
print(h1_octal)
print(type(h1_octal))
# Cộng tổng của Int Hex và Octal
print(1 + 0x1 + 0o1)
# abs giá trị tuyệt đối
print(abs(4))
print(abs(-4))
# round() làm tròn số
print(round(8.4))
print(round(8.5))
print(round(8.6))
# Chuyển đổi giữa các kiểu dữ liệu
print(bin(8))
print(hex(8))
